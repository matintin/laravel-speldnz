								<p>This Level 3 introductory course is offered for parents, caregivers, teachers, teacher aides or anyone interested in learning about specific learning disabilities. It provides a non-technical overview of Dyslexia and Specific Learning Disabilities (SLD) and an insight into what it is like to have a learning disability.</p>
								<p>This is an introductory course, suitable for those who limited knowledge about SLD. It gives an insight into why specific learning disabilities occur, what it is like to have SLD and offers some initial strategies for helping these learners in the classroom and at home. For a more in depth course please talk with us about our Level 5 Certificate Course in Specific Learning Disabilities.</p>
								<p>This is a two-day course with activities to complete out of Course time. A certificate is issued on completion of the Course requirements.</p>
								<p>Course topics include:</p>
								<ul>
									<li>How it feels to have a specific learning disability</li>
									<li>How failure leads to frustration and trouble in the classroom</li>
									<li>How the brain works</li>
									<li>Literacy</li>
									<li>Numeracy</li>
								</ul>
								<p>No prior qualifications are needed to enrol in this course.</p>

								<h3>SPELD NZ Assessor Training</h3>

								<p>The following Assessor Training is not an NZQA-approved course.</p>
								<p>This training is usually held once a year and is aimed towards training applicants who have the required NZCER Level C qualifications, in the administration and interpretation of the Woodcock-Johnson test batteries.  Our 2016 assessor training will focus on the soon to be released WJIV version and we are now updating course content.</p>
								<p>Currently the training is in two parts: theory and mentored practicum. The theory component involves assignments and a three and a half-day workshop. Those who successfully complete the theory component are invited to apply for the mentored practicum programme and register as a probationary SPELD NZ Assessor. Upon successful completion of the probationary period a full SPELD NZ assessor certificate is issued.</p>
								<p>Topics covered include:</p>
								<ul>
									<li>use of the Woodcock-Johnson assessment batteries including test administration and interpretation of data</li>
									<li>an understanding of SPELD NZ as an organisation</li>
									<li>Identifying those with specific learning disabilities</li>
								</ul>

