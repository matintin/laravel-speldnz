								<span>Approved by New Zealand Qualifications Authority (NZQA) and rated "Highly Confident in educational performance."  SPELD NZ is accredited to deliver the Course.</span>
								<p>This Level 5 course available to New Zealand certificated secondary, primary, and Early Childhood Teachers, or those with a tertiary qualification and experience in working with children, who wish to extend their knowledge and expertise in teaching, or learning about, people with Specific Learning Disabilities (SLD).</p> 
								<p>Following the successful completion of the certificate course, those participants that hold a teaching qualification and have a minimum of one year's classroom teaching experience can apply to become a SPELD NZ teacher working one-to-one with students with SLD referred by SPELD NZ.</p>
								<p>The objectives of the Course are to:</p>
								
								<ul>
									<li>Provide ways of identifying students with Dyslexia and other Specific Learning Disabilities within various age ranges.</li>
									<li>Provide practical classroom strategies to better meet the needs of students with Dyslexia and Specific Learning Disabilities.</li>
									<li>Equip teachers to design and implement a classroom teaching programme that will meet the needs of the student with Dyslexia and Specific Learning Disabilities and the needs of the whole class.</li>
									<li>Equip teachers to support the student with Dyslexia and other SLD.</li>
									<li>Equip teachers to interpret specialist reports as a result of the educational assessment Woodcock-Johnson III.</li>
								</ul>
								
								<p>Areas covered in the Certificate Course include:</p>
								<ul>
									<li>Signs of SLD in the 5-7, 7-13, 13-18 and adult age groups.</li>
									<li>Overview of theory development</li>
									<li>Child development and exceptional children</li>
									<li>An Introduction to neurology</li>
									<li>An Introduction to neurology</li>
									<li>A meuro-psychological introduction to SLD</li>
									<li>Values, materials and uses of different play media related to age development and interest of children</li>
									<li>Dyslexia in adolescence</li>
									<li>Self help strategies for students</li>
									<li>Effects on families/whanau</li>
									<li>Behaviour and emotions</li>
									<li>Auditory and visual processes</li>
									<li>Assessment and lesson planning/li>
									<li>Reading, spelling, writing and maths with SLD</li>
									<li>Remedial theories</li>
									<li>Sensory integration and motor development</li>
									<li>Teaching learners with dyslexia _ children to adults</li>
									<li>Technology and SLD</li>
									<li>Assessment and interpretation</li>
									<li>Lesson planning from an assessment report</li>
								</ul>
								
