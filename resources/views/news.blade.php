			<h3>"Success for SPELD NZ"</h3><div class="empty"></div><div class="image-wrap"><img src="asset/icons/plus.png" alt=""></div>
			<div class="accordion-news">
				<p>Pilot Research Program: looking at ourselves</p>
				<p>At the 2012 SPELD NZ Conference in Palmerston North we received some excellent news.  Dr Karen Waldie, a noted researcher, spoke about the results of the SPELD NZ Pilot Project(1).  Dr Waldie is an Associate Professor in the Department of Psychology and Research Centre for Cognitive Neuroscience at the University of Auckland.</p>
				<p>Having a well-regarded and independent academic analyse the results of the Pilot Project gives those results real credibility.  SPELD NZ professionals have the goal of making a difference for those with dyslexia.  These results indicate that the work and approach of SPELD NZ is at the very least heading in the right direction.</p>
				<p>In the course of her Conference presentation Dr Waldie commented that she had been impressed by the degree of difference that the research results highlighted. </p>
				<p>The Pilot Project had a sample size of 42 seven year old students. These students received a ‘before’ assessment using the Woodcock-Johnson III (WJIII) test battery, then 60forty-five minute sessions of 1:1 tuition, twice weekly. This remedial intervention was followed by another assessment using the same WJIII test battery.</p>
				<p>Although the sample size of 42 is not large, the analysis of the findings from the SPELD NZ Pilot Project is very encouraging indeed.  Ideally a similar project will be repeated in the future so as to demonstrate even more strongly the positive outcomes of SPELD NZ work.  It is hoped that these results can be used to support applications for funding for further research.</p>
				<p>At that time a similar key question would be asked, i.e. “Do students who are given SPELD tuition, perform significantly better at the second assessment, than they did at their first, for each Cognitive and Achievement test?” </p>
				<p>As noted above, when that question was asked in this Pilot Project, the results were very pleasing for all concerned.</p>
				<p>Dr Waldie carried out detailed statistical analyses on each test or cluster and on each dependent variable to show the effectiveness of SPELD NZ teaching. (A more detailed and academic report than is given here, will be available soon on the SPELD NZ website).</p>
				<p>In a summary form some of the main findings reported on included:</p>
				
				<ul>
					<li>- All cognitive abilities improved significantly</li>
					<li>- There were small but useful increases for students in verbal ability, phonemic awareness, cognitive efficiency and processing speed.</li>
					<li>- There was an improvement in general intellectual ability.</li>
					<li>- There was a significant improvement in thinking ability.  Thinking ability covers visual-auditory learning, spatial relations, sound blending, concept formation, retrieval fluency, picture recognition, auditory attention and Analysis-Synthesis.  This is a particularly useful area in which to be able to demonstrate student gains.</li>
					<li>- Visual matching, number reversal, decision speed and memory for words aspects of cognitive efficiency did not show significant improvement.</li>
					<li>- Student phonemic awareness showed significant development.</li>
				</ul>

				<p>Dr Waldie also carried out statistical analysis on the Success Rate Achievement results. </p>
				<p>All achievement skills improvedsignificantly.</p>
				<p>Broad reading skills, word identification and sounding out skills showed the most change.  Oral language, listening and spelling skills also showed development but not to the same degree. Overall, word attack scores improved significantly.</p>
				<p>That statistical analysis shows such satisfactory results for SPELD NZ intervention also has wider implications for students. Success leads to confidence and this is exemplified by many of the comments received at the end of the Pilot research programme.  Typical (unedited) comments included:</p>
				
				<ul>
					<li>- “I feel better on the inside now.”</li>
					<li>- “will happily write now”</li>
					<li>- “massive improvements in behaviour and attitude”</li>
					<li>- “Confidence has improved”.</li>
					<li>- “He feels much happier to attempt to read words for himself”.</li>
					<li>- “He feels proud when he achieves things for himself.”</li>
				</ul>

				<p>SPELD NZ has something to be proud of with the results of this research.  Although it is a first step, it indicates that the SPELD NZ approach is built on strong foundations and can make a strong contribution to those with dyslexia and to the wider education sector.</p>
				<p>These research results will be publicized and used to develop and underpin future work.  The time, energy and thought put into this project has resulted in SPELD NZ being able to look forward with confidence, just as those students involved in this piece of research can. </p>
				<p>(1)This article is closely based on the presentation of Dr Waldie. However, the comments and interpretation are the sole responsibility of the writer.</p>
				<p class="source">&lt;Jack Austin, Psychologist and Board Member, SPELD NZ&gt; </p>
