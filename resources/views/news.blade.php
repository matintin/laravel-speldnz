			<h3>Pilot Research Project shows "vast improvements" for SPELD 
				NZ students</h3>
			<p>In 2011 SPELD NZ carried out a Pilot Programme in collaboration with 
				RTLBs. The data obtained from this Pilot Project was analysed by Dr 
				Karen Waldie of Auckland University. Dr Waldie said she didn't realise 
				just how successful the Pilot Project would be: "Not only are children 
				increasing their predicted reading success by 20-44% in areas of sound 
				blending, phonemic awareness, verbal comprehension and reading fluency, 
				but their general cognitive abilities also significantly improved.  
				We saw vast improvements in thinking ability, cognitive fluency and 
				processing speed."</p>
			<p>In July 2014, the New Zealand Journal of Educational Studies published 
				an article about this study.  The article concludes that SPELD NZ 
				teaching was effective in improving the academic skills of children 
				with reading disabilities and also significantly increased overall 
				intellectual ability.</p>
