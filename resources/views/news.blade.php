			<h3>"Does SPELD Intervention work?" </h3><div class="empty"></div><div class="image-wrap"><img src="asset/icons/plus.png" alt=""></div>
			<div class="accordion-news">
				<p>In 2009 the SPELD NZ Board commissioned the New Zealand Council for Educational Research to document the effectiveness of SPELD teaching methods by surveying classroom teachers, RTLB, parents, SPELD teachers and Assessors. Parts 1 & 2 of the Effectiveness Review were published in 2010. Previous Board President Rodney Barber realised that although the Board knew SPELD teachers were highly trained to assist children and adults with Specific Learning Disabilities (SLD) including dyslexia, what was still needed was statistical proof. So he set out to prove it.</p>
				<p>With the support of the RTLB Association, The Principals’ Federation, SPELD teachers and assessors around New Zealand, our group of 42 students were selected and assessed. Teaching began in Term 1, 2011. The children were from different schools and circumstances; from decile one (fully funded by SPELD financial assistance) to decile ten (with full payment from parents). Reassessments were completed after 60 lessons and the data has been assessed by Dr Karen Waldie, Associate Professor, School of Psychology, Auckland University.</p>
				<p>This is what Dr Waldie has to say.</p>
				<i class="fa fa-quote-left"></i>
				<p>I agreed to analyse the SPELD NZ pilot data back in 2010 because I believe in SPELD and know that children with dyslexia do better with this than any other remediation programme I'm aware of.  I knew that I would find that students would increase their reading skills after SPELD lessons. What I didn't realize, however, was just how successful the pilot programme would be. Not only are children increasing their predicted reading success by 20-44% in areas of sound blending, phonemic awareness, verbal comprehension and reading fluency, but their general cognitive abilities also significantly improved. We saw vast improvements in thinking ability, cognitive fluency and processing speed. I am truly impressed!</p>
				<i class="fa fa-quote-right"></i>
				<p>SPELD NZ thanks Dr Waldie for her detailed analysis of our results.  See Page 4 of this newsletter for a summary written by Dunedin Psychologist and SPELD Board member Jack Austin.</p>
				<p>Professor John Hattie from Melbourne Education Research Institute has kindly agreed to add his interpretation of the data and this will be available soon.  The full results will be published in an academic journal and on the SPELD website in due course.</p>
				<p>The pilot project has proven that by investing in our underachieving seven year old children, we can help them to achieve literacy and success at school. As SLD can be hereditary, this would have a flow on intergenerational benefit. </p>
				<p>Can we afford NOT to help these children?</p>
				<p class="source">&lt;The articles about the Pilot were printed 
					in the November 2012 issue of our newsletter, The Record.&gt;</p>
